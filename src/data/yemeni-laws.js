// Helper functions
export const getLawsByCategory = (category) => {
    const allLaws = []
    Object.values(yemeniLaws).forEach(lawCategory => {
        lawCategory.articles.forEach(article => {
            if (article.category === category) {
                allLaws.push({
                    ...article,
                    lawTitle: lawCategory.title
                })
            }
        })
    })
    return allLaws
}

export const searchLaws = (searchTerm) => {
    const results = []
    Object.values(yemeniLaws).forEach(lawCategory => {
        lawCategory.articles.forEach(article => {
            if (
                article.text.includes(searchTerm) ||
                article.category.includes(searchTerm) ||
                article.number.includes(searchTerm)
            ) {
                results.push({
                    ...article,
                    lawTitle: lawCategory.title
                })
            }
        })
    })
    return results
}

export const getLawCategories = () => {
    const categories = new Set()
    Object.values(yemeniLaws).forEach(lawCategory => {
        lawCategory.articles.forEach(article => {
            categories.add(article.category)
        })
    })
    return Array.from(categories)
}